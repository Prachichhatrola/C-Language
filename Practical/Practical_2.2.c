int main()
{
    int a,b;
    printf("Enter number a");
    scanf("%d",&a);
    printf("Enter number b");
    scanf("%d",&b);
    printf("addition of a+b=%d\n",a+b);
    printf("subtraction of a-b=%d\n",a-b);
    printf("multiplication of a*b=%d\n",a*b);
    printf("division of a%b=%d\n",a/b);
    printf("modulo of a%b=%d\n",a%b);
    return 0;
}
